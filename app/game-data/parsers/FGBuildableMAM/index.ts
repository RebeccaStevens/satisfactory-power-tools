import { Effect, pipe } from "effect";

import {
  type FGBuildable,
  parseFGBuildable,
} from "~/game-data/parsers/FGBuildable";
import type { ParseError } from "~/game-data/parsers/errors";

import { assertVendorFGBuildableMAM } from "./assert";

export function parseFGBuildableMAM(
  data: unknown,
): Effect.Effect<FGBuildableMAM, ParseError> {
  assertVendorFGBuildableMAM(data);

  return pipe(
    Effect.all([parseFGBuildable(data), pipe(Effect.all({}))]),

    Effect.andThen(([item, extraProps]) => ({
      ...item,
      ...extraProps,
    })),
  );
}

export type FGBuildableMAM = FGBuildable & {};
