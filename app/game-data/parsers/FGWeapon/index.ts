import { Effect, pipe } from "effect";

import {
  type AbstractEquipment,
  parseAbstractEquipment,
} from "~/game-data/parsers/abstractEquipment";
import type { ParseError } from "~/game-data/parsers/errors";

import { assertVendorFGWeapon } from "./assert";

export function parseFGWeapon(
  data: unknown,
): Effect.Effect<FGWeapon, ParseError> {
  assertVendorFGWeapon(data);

  return pipe(
    Effect.all([parseAbstractEquipment(data), pipe(Effect.all({}))]),

    Effect.andThen(([item, extraProps]) => ({
      ...item,
      ...extraProps,
    })),
  );
}

export type FGWeapon = AbstractEquipment & {};
