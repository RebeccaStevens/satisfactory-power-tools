import { Effect, pipe } from "effect";

import { type FGBuildable, parseFGBuildable } from "~/game-data/generate/parsers/FGBuildable";
import type { ParseError } from "~/game-data/generate/parsers/errors";

import { assertVendorFGBuildablePassthrough } from "./assert";

export function parseFGBuildablePassthrough(data: unknown): Effect.Effect<FGBuildablePassthrough, ParseError> {
  assertVendorFGBuildablePassthrough(data);

  return pipe(
    Effect.all([parseFGBuildable(data), pipe(Effect.all({}))]),

    Effect.andThen(([item, extraProps]) => ({
      ...item,
      ...extraProps,
    })),
  );
}

export type FGBuildablePassthrough = FGBuildable & {};
